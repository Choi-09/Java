// if 조건문
package edu;

public class Test21 {

	public static void main(String[] args) {
		int a = 120;
		int b = 4;
		char op = '/';
		
		if(op == '+') {
			System.out.println(a+b);
		} else if(op == '-') {
			System.out.println(a-b);
		} else if(op == '*' ) {
			System.out.println(a*b);
		} else if(op == '/') {
			System.out.println(a/b);
		}

	}

}

